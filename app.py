from tkinter import Tk, Canvas
from typing import Any, Callable, List, Tuple

Point = Tuple[float | int, float | int]


def draw_oval(
    coords: Tuple[Point, ...],
    fill: str | None = None,
    outline: str | None = None,
    width: float | None = None
):
    canvas_items.append(canvas.create_oval(
        coords,
        fill=fill or '',
        outline=outline or '#000',
        width=width or 0
    ))


def draw_rect(
    coords: Tuple[Point, ...],
    fill: str | None = None,
    outline: str | None = None,
    width: float | None = None
):
    canvas_items.append(canvas.create_rectangle(
        coords,
        fill=fill or '',
        outline=outline or '#000',
        width=width or 0
    ))


def draw_polygon(
    coords: Tuple[Point, ...],
    fill: str | None = None,
    outline: str | None = None,
    width: float | None = None
):
    outline = outline or ('#000' if width else '')
    canvas_items.append(canvas.create_polygon(
        coords,
        fill=fill or '',
        outline=outline,
        width=width or 0
    ))


def draw_path(
    coords: Tuple[Point, ...],
    outline: str | None = None,
    width: float | None = None
):
    for (i, point) in enumerate(coords):
        if i == 0:
            continue
        canvas_items.append(canvas.create_line(
            (coords[i - 1], point),
            fill=outline or '#000',
            width=width or ''
        ))


def repeat_draw(
    func: Callable,
    coords: Tuple[Tuple[Point, ...], ...],
    **kwargs
):
    for coord in coords:
        func(coord, **kwargs)


def is_number(x: Any):
    return type(x) == int or type(x) == float


def with_shift(
    shift: Point,
    coords: Tuple[Point | Tuple[Point, ...], ...]
) -> Tuple[Any]:
    def transform(x: Point | Tuple[Point, ...]):
        match x:
            case [int() | float(), int() | float()]:
                return x[0] + shift[0], x[1] + shift[1]
            case _:
                return with_shift(shift, x)

    return tuple(map(transform, coords))


